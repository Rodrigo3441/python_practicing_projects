#tuples (immutable and ordered), can not be modified once created
point = (0,1,2,3,4,5,1,7,1,9,10,1)
print(point)
print(point[2])

#methods
#count(element) -> return the number of times a element appear in a tuple
#index(*element, startposition, endposition) -> returns the index of the first occurrence of an element
#len(tuple) -> returns the size of the tuple, and others structures

print('tuple size:', len(point))
print(point.count(1))
print(point.index(1)) 


